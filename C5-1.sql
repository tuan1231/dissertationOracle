/*5.1 Tạo Sequence*/
/*
	Câu 1: Tạo mã số tự động (Sequence) cho bảng HOCHAM
*/
CREATE SEQUENCE S_551_CAU1
	INCREMENT BY		1
	START WITH			1
	MINVALUE				1
	MAXVALUE				1000000000000000000000000000
	CYCLE;
/
SELECT * FROM HOCHAM;	
/
SELECT S_551_CAU1.NEXTVAL AS STT, MSHH, TENHH FROM HOCHAM;
/
SELECT S_551_CAU1.CURRVAL FROM DUAL;
